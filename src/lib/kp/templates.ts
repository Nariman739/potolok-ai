import type { KpConfig, KpSection, KpTemplateId } from "./types";

// Дефолтные kpConfig для каждой темы — задают и порядок секций, и
// стартовый контент (тексты гарантий + FAQ под сегмент темы).
// При клике на тему-стартер этот конфиг целиком записывается в Master.kpConfig,
// дальше мастер правит под себя.

const SECTIONS_DEFAULT_ORDER: Omit<
  KpSection,
  "items" | "title" | "body"
>["type"][] = [
  "cover",
  "breakdown",
  "portfolio",
  "warranties",
  "reviews",
  "faq",
  "about",
  "contacts",
];

function buildSections(args: {
  warranties: { title: string; value: string }[];
  faq: { q: string; a: string }[];
  about?: { title: string; body: string };
  enabledAbout?: boolean;
}): KpSection[] {
  return SECTIONS_DEFAULT_ORDER.map((type): KpSection => {
    switch (type) {
      case "cover":
        return { type: "cover" };
      case "breakdown":
        return { type: "breakdown" };
      case "portfolio":
        return { type: "portfolio", enabled: true };
      case "warranties":
        return { type: "warranties", enabled: true, items: args.warranties };
      case "reviews":
        return { type: "reviews", enabled: true };
      case "faq":
        return { type: "faq", enabled: true, items: args.faq };
      case "about":
        return {
          type: "about",
          enabled: !!args.enabledAbout,
          title: args.about?.title ?? "О нашей компании",
          body: args.about?.body ?? "",
        };
      case "contacts":
        return { type: "contacts" };
    }
  });
}

