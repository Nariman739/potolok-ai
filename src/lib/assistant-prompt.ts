// Утилита: генерация сводки по комнатам
// Вызывается из route.ts, НЕ тратит AI-токены
// ─────────────────────────────────────────────────────
export function computeRoomSummary(visionDataJson: string): string {
  try {
    const data = JSON.parse(visionDataJson) as {
      rooms?: Array<{
        name: string;
        area?: number | null;
        length?: number | null;
        width?: number | null;
        perimeter?: number | null;
        spotsCount?: number | null;
        curtainRodLength?: number | null;
        notes?: string | null;
        rectangles?: Array<{ width: number; height: number; area: number }> | null;
      }>;
      totalArea?: number | null;
    };

    const rooms = data.rooms || [];
    if (rooms.length === 0) return "";

    const lines: string[] = [];
    let calcTotal = 0;
    let allExact = true;

    for (const room of rooms) {
      const hasLW = !!(room.length && room.width);

      // Area: prefer rectangles sum > document area > bounding box
      let area: number | null = null;

      // 1. If rectangles provided — sum them (most accurate, AI showed its work)
      if (room.rectangles && room.rectangles.length > 0) {
        const rectSum = Math.round(room.rectangles.reduce((s, r) => s + r.area, 0) * 100) / 100;
        // Validate against bounding box
        if (hasLW) {
          const boundingBox = Math.round(room.length! * room.width! * 100) / 100;
          area = rectSum <= boundingBox * 1.05 ? rectSum : boundingBox;
        } else {
          area = rectSum;
        }
      } else if (hasLW) {
        // 2. Sanity-check document area against bounding box
        const boundingBox = Math.round(room.length! * room.width! * 100) / 100;
        if (room.area != null && room.area <= boundingBox * 1.05) {
          area = room.area;
        } else if (room.area != null && room.area > boundingBox * 1.05) {
          area = boundingBox; // AI overcomputed — fall back to bounding box
          allExact = false;
        } else {
          area = boundingBox;
        }
      } else if (room.area != null) {
        // 3. Only area provided, no dimensions
        area = room.area;
      }

      if (!hasLW && room.area == null) allExact = false;

      // Perimeter priority: 1) explicit perimeter field (AI-computed, handles L-shapes), 2) 2×(L+W) for rectangles, 3) estimate from area
      let perimeter: number | null = null;
      let perimExact = false;

      if (room.perimeter != null) {
        perimeter = room.perimeter;
        perimExact = true;
      } else if (hasLW) {
        perimeter = Math.round(2 * (room.length! + room.width!) * 10) / 10;
        perimExact = true;
      } else if (area) {
        // estimate via 1.3:1 room proportion
        const l = Math.sqrt(area * 1.3);
        const w = Math.sqrt(area / 1.3);
        perimeter = Math.round(2 * (l + w) * 10) / 10;
        perimExact = false;
      }

      if (area) calcTotal += area;

      const areaStr = area != null ? `${area} м²` : "площадь неизвестна";
      const perimStr =
        perimeter != null
          ? `, периметр ${perimExact ? "" : "~"}${perimeter.toFixed(1)} м.п.`
          : "";
      const dimsStr = hasLW ? ` (${room.length}×${room.width}м)` : "";
      const spotsStr = room.spotsCount ? `, ${room.spotsCount} спот.` : "";
      const trackStr = room.curtainRodLength ? `, трек ${room.curtainRodLength}м` : "";
      const notesStr = room.notes ? ` [${room.notes}]` : "";

      lines.push(`• ${room.name} — ${areaStr}${dimsStr}${perimStr}${spotsStr}${trackStr}${notesStr}`);
    }

    const totalArea = data.totalArea ?? calcTotal;
    const n = rooms.length;
    const roomWord = n === 1 ? "комнату" : n < 5 ? "комнаты" : "комнат";

    const parts = [
      `Нашёл ${n} ${roomWord}:`,
      ...lines,
      `\nОбщая площадь: ${totalArea.toFixed(1)} м²`,
    ];

    if (!allExact) {
      parts.push(
        "⚠️ Периметры приблизительные (точные размеры стен в паспорте не указаны)"
      );
    } else {
      parts.push("Размеры точные ✓");
    }

    return parts.join("\n");
  } catch {
    return "";
  }
}

